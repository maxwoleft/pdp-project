async def amain() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--apply", action="store_true")
    parser.add_argument("--replace", action="store_true")
    args = parser.parse_args()

    engine = build_engine()
    factory = build_session_factory(engine)
    created = 0
    skipped_no_keys = 0

    try:
        async with factory() as session:
            if args.replace and args.apply:
                await session.execute(
                    delete(ServiceProfile).where(ServiceProfile.created_by == "remaining_v1")
                )
                await session.flush()

            existing_keys = {
                r[0] for r in (await session.execute(
                    select(ServiceProfile.canonical_key)
                    .where(ServiceProfile.canonical_key.isnot(None))
                )).all() if r[0]
            }

            all_real: set[str] = set()
            for c in ("ua", "pl", "gb"):
                rows = await session.execute(text(
                    f"SELECT DISTINCT canonical_key FROM {c}.service "
                    "WHERE archive=false AND canonical_key IS NOT NULL"
                ))
                all_real.update(r[0] for r in rows.all() if r[0])

            for d in PROFILES:
                name = d["name"]
                keys = [k for k in d.get("canonical_keys", []) if k in all_real]
                missing = [k for k in d.get("canonical_keys", []) if k not in all_real]
                if missing:
                    print(f"  ⚠ {name}: missing {missing}")
                if not keys:
                    print(f"  ✖ {name}: NO VALID KEYS — SKIP")
                    skipped_no_keys += 1
                    continue
                primary = keys[0]
                if primary in existing_keys:
                    primary = f"fam_rem_{uuid.uuid4().hex[:10]}"
                pid = str(uuid.uuid4())
                print(f"  → {name:55s} keys={len(keys)}")
                if not args.apply:
                    continue
                session.add(ServiceProfile(
                    id=pid, canonical_key=primary, name=name,
                    country="ua", default_language="uk", enabled=True,
                    created_by="remaining_v1", updated_by="remaining_v1",
                ))
                session.add(ServiceProfileTranslation(
                    profile_id=pid, language="uk",
                    short_description=d["short_description"],
                    addresses_problems=d.get("addresses_problems", []),
                    target_audience=d.get("target_audience", []),
                    benefits=d.get("benefits", []),
                    keywords=d.get("keywords", []),
                    sales_pitch=d.get("sales_pitch"),
                    cross_sell=d.get("cross_sell", []),
                    procedure_steps=d.get("procedure_steps", []),
                    contraindications=d.get("contraindications", []),
                    aftercare_advice=d.get("aftercare_advice"),
                ))
                session.add(ServiceProfileOption(
                    profile_id=pid, option_type="family",
                    name=name, sort_order=0,
                    short_description=d["short_description"],
                    addresses_problems=d.get("addresses_problems", []),
                    target_audience=d.get("target_audience", []),
                    benefits=d.get("benefits", []),
                    keywords=d.get("keywords", []),
                    sales_pitch=d.get("sales_pitch"),
                    cross_sell=d.get("cross_sell", []),
                    procedure_steps=d.get("procedure_steps", []),
                    contraindications=d.get("contraindications", []),
                    aftercare_advice=d.get("aftercare_advice"),
                    canonical_keys=keys,
                ))
                for i, opt in enumerate(d.get("brand_options") or [], 1):
                    opt_keys = [k for k in opt.get("canonical_keys", []) if k in all_real]
                    if not opt_keys:
                        continue
                    session.add(ServiceProfileOption(
                        profile_id=pid, option_type="brand",
                        name=opt["name"], sort_order=i,
                        short_description=opt.get("short_description", ""),
                        addresses_problems=opt.get("addresses_problems", []),
                        target_audience=opt.get("target_audience", []),
                        benefits=opt.get("benefits", []),
                        keywords=opt.get("keywords", []),
                        sales_pitch=opt.get("sales_pitch"),
                        cross_sell=opt.get("cross_sell", []),
                        procedure_steps=opt.get("procedure_steps", []),
                        contraindications=opt.get("contraindications", []),
                        aftercare_advice=opt.get("aftercare_advice"),
                        canonical_keys=opt_keys,
                    ))
                existing_keys.add(primary)
                created += 1

            if args.apply:
                await session.commit()
                print(f"\nDONE: created {created} (skipped no-keys: {skipped_no_keys})")
            else:
                print(f"\nDRY RUN")
    finally:
        await engine.dispose()


if __name__ == "__main__":
    asyncio.run(amain())
