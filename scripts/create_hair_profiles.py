async def amain() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--apply", action="store_true")
    parser.add_argument("--replace", action="store_true", help="видалити всі профілі created_by=hair_v1 перед створенням")
    args = parser.parse_args()

    engine = build_engine()
    factory = build_session_factory(engine)
    created = 0
    skipped = 0

    try:
        async with factory() as session:
            if args.replace and args.apply:
                # Видалити попередні створені цим скриптом
                await session.execute(
                    delete(ServiceProfile).where(ServiceProfile.created_by == "hair_v1")
                )
                await session.flush()

            existing_keys = {
                r[0] for r in (await session.execute(
                    select(ServiceProfile.canonical_key)
                    .where(ServiceProfile.canonical_key.isnot(None))
                )).all() if r[0]
            }

            for definition in HAIR_PROFILES:
                name = definition["name"]
                keys = definition["canonical_keys"]
                primary = keys[0] if keys else None
                if primary and primary in existing_keys:
                    # Якщо ключ уже зайнятий — генеруємо synthetic
                    primary = f"fam_hair_{uuid.uuid4().hex[:10]}"

                profile_id = str(uuid.uuid4())
                print(f"  → {name:50s} keys={len(keys)}")

                if not args.apply:
                    continue

                session.add(ServiceProfile(
                    id=profile_id, canonical_key=primary, name=name,
                    country="ua", default_language="uk", enabled=True,
                    created_by="hair_v1", updated_by="hair_v1",
                ))
                session.add(ServiceProfileTranslation(
                    profile_id=profile_id, language="uk",
                    short_description=definition["short_description"],
                    addresses_problems=definition.get("addresses_problems", []),
                    target_audience=definition.get("target_audience", []),
                    benefits=definition.get("benefits", []),
                    keywords=definition.get("keywords", []),
                    sales_pitch=definition.get("sales_pitch"),
                    cross_sell=definition.get("cross_sell", []),
                    procedure_steps=definition.get("procedure_steps", []),
                    contraindications=definition.get("contraindications", []),
                    aftercare_advice=definition.get("aftercare_advice"),
                ))
                # Family option з ВСІМА canonical_keys
                session.add(ServiceProfileOption(
                    profile_id=profile_id, option_type="family",
                    name=name, sort_order=0,
                    short_description=definition["short_description"],
                    addresses_problems=definition.get("addresses_problems", []),
                    target_audience=definition.get("target_audience", []),
                    benefits=definition.get("benefits", []),
                    keywords=definition.get("keywords", []),
                    sales_pitch=definition.get("sales_pitch"),
                    cross_sell=definition.get("cross_sell", []),
                    procedure_steps=definition.get("procedure_steps", []),
                    contraindications=definition.get("contraindications", []),
                    aftercare_advice=definition.get("aftercare_advice"),
                    canonical_keys=keys,
                ))

                # Brand options
                for i, opt in enumerate(definition.get("brand_options") or [], 1):
                    session.add(ServiceProfileOption(
                        profile_id=profile_id, option_type="brand",
                        name=opt["name"], sort_order=i,
                        short_description=opt.get("short_description", ""),
                        addresses_problems=opt.get("addresses_problems", []),
                        target_audience=opt.get("target_audience", []),
                        benefits=opt.get("benefits", []),
                        keywords=opt.get("keywords", []),
                        sales_pitch=opt.get("sales_pitch"),
                        cross_sell=opt.get("cross_sell", []),
                        procedure_steps=opt.get("procedure_steps", []),
                        contraindications=opt.get("contraindications", []),
                        aftercare_advice=opt.get("aftercare_advice"),
                        canonical_keys=opt.get("canonical_keys", []),
                    ))

                existing_keys.add(primary)
                created += 1

            if args.apply:
                await session.commit()
                print(f"\nDONE: created {created}, skipped {skipped}")
            else:
                print(f"\nDRY RUN: would create {len(HAIR_PROFILES)} profiles")
    finally:
        await engine.dispose()


if __name__ == "__main__":
    asyncio.run(amain())
