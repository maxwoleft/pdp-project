async def amain():
    engine = build_engine()
    factory = build_session_factory(engine)
    embedder = OpenAIEmbedder()

    created = 0
    try:
        async with factory() as session:
            repo = ServiceProfileRepository(session, embedder=embedder)
            for cat in CATEGORIES:
                existing = await repo.get_by_canonical_key(cat["canonical_key"])
                if existing:
                    print(f"  SKIP {cat['name']} (exists)")
                    continue

                profile = await repo.create(
                    canonical_key=cat["canonical_key"],
                    name=cat["name"],
                    country=None,
                    default_language="uk",
                    enabled=True,
                    created_by="category_seed",
                    updated_by="category_seed",
                )

                tr = cat["translation"]
                await repo.upsert_translation(
                    profile.id,
                    "uk",
                    short_description=tr["short_description"],
                    addresses_problems=tr.get("addresses_problems", []),
                    benefits=tr.get("benefits", []),
                    keywords=tr.get("keywords", []),
                    sales_pitch=tr.get("sales_pitch"),
                    cross_sell=tr.get("cross_sell", []),
                )
                await repo.save_version(
                    profile.id,
                    change_summary="Category profile created",
                    created_by="category_seed",
                )
                created += 1
                print(f"  CREATE {cat['name']}")

            await session.commit()
    finally:
        await engine.dispose()
    print(f"\nDone: {created} category profiles created")


if __name__ == "__main__":
    asyncio.run(amain())
