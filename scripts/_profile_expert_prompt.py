# ── Trigger words validator (post-generation safety) ────────────

FORBIDDEN_WORDS = [
    # Medical claims
    "лікує", "лікує",  "виліковує", "виліковує", "знищує", "усуває назавжди",
    "омолоджує клітини", "стимулює ріст", "гарантує",
    # Fake urgency
    "лише сьогодні", "тільки сьогодні", "залишилось 2", "встигніть",
    # Body shaming
    "ви будете гарнішою", "ви будете гарнішим", "позбудетесь зайвого",
    "позбавляє назавжди", "позбавитесь недоліків", "приховає недоліки",
    # Sexist
    "чоловік буде в захваті", "чоловіки оцінять",
    # Absolute claims
    "100% результат", "100% ефект", "безболісно", "повністю безпечно",
    "підходить усім",
]


def find_forbidden(text: str) -> list[str]:
    """Шукає заборонені слова. Повертає список знайдених (для логів)."""
    if not text:
        return []
    lower = text.lower()
    return [w for w in FORBIDDEN_WORDS if w in lower]


REQUIRED_FIELDS = {
    "short_description": str,
    "addresses_problems": list,
    "target_audience": list,
    "benefits": list,
    "keywords": list,
    "sales_pitch": str,
    "cross_sell": list,
    "procedure_steps": list,
    "contraindications": list,
    "aftercare_advice": (str, type(None)),
}


def validate_response(data: dict) -> list[str]:
    """Повертає список помилок (пустий = валідно)."""
    errors = []
    for field, expected_type in REQUIRED_FIELDS.items():
        if field not in data:
            errors.append(f"missing field: {field}")
            continue
        val = data[field]
        if isinstance(expected_type, tuple):
            if not isinstance(val, expected_type):
                errors.append(f"{field}: wrong type {type(val).__name__}")
        else:
            if not isinstance(val, expected_type):
                errors.append(f"{field}: wrong type {type(val).__name__}")

    # Довжини обмежень
    if isinstance(data.get("short_description"), str) and len(data["short_description"]) > 350:
        errors.append("short_description too long (>350)")
    if isinstance(data.get("sales_pitch"), str) and len(data["sales_pitch"]) > 380:
        errors.append("sales_pitch too long (>380)")

    # Trigger words в текстових полях
    for field in ("short_description", "sales_pitch", "aftercare_advice"):
        val = data.get(field)
        if isinstance(val, str):
            forbidden = find_forbidden(val)
            if forbidden:
                errors.append(f"{field}: forbidden words {forbidden}")

    # Trigger words у списках
    for field in ("benefits", "addresses_problems", "target_audience"):
        for item in data.get(field, []) or []:
            if isinstance(item, str):
                forbidden = find_forbidden(item)
                if forbidden:
                    errors.append(f"{field}[item]: forbidden words {forbidden}")

    return errors
